/**
 * @module services/pr-template
 *
 * Builds GitHub pull-request titles and bodies that follow the project standard.
 *
 * Title format:  [Task-ID] Task Title
 * Body format:   Structured Markdown with ADO link, Figma link, change list,
 *                screenshot placeholder, and test-case checklist.
 */

/**
 * Generates a PR title.
 *
 * @param {number|string} taskId
 * @param {string}        taskTitle
 * @returns {string}  e.g. "[123] Add employee management form"
 */
export const buildPRTitle = (taskId, taskTitle) => {
  return `[${taskId}] ${taskTitle}`;
};

/**
 * Generates a full PR body from task details and optional metadata.
 *
 * @param {object}   options
 * @param {number}   options.taskId
 * @param {string}   options.taskTitle
 * @param {string}   [options.taskType="Task"]
 * @param {string}   [options.taskUrl=""]
 * @param {string}   [options.summary=""]            Human-readable summary
 * @param {string[]} [options.changes=[]]             List of changes made
 * @param {string}   [options.figmaUrl=""]            Figma design URL
 * @param {string[]} [options.testCases=[]]           Test checklist items
 * @param {string}   [options.branchName=""]
 * @returns {string}  Markdown PR body
 */
export const buildPRDescription = ({
  taskId,
  taskTitle,
  taskType = "Task",
  taskUrl = "",
  summary = "",
  changes = [],
  figmaUrl = "",
  testCases = [],
  branchName = "",
}) => {
  const changesList = changes.length
    ? changes.map((c) => `- ${c}`).join("\n")
    : "- See diff for the full list of changes";

  const testList = testCases.length
    ? testCases.map((t) => `- [ ] ${t}`).join("\n")
    : "- [ ] Manual testing completed\n- [ ] Existing tests pass";

  const figmaSection = figmaUrl
    ? `\n## Design\n- Figma: ${figmaUrl}\n`
    : "";

  return `## Summary
${summary || `Implements ${taskType} #${taskId}: ${taskTitle}`}

## Azure DevOps
- **${taskType}:** [#${taskId} — ${taskTitle}](${taskUrl})
- **Branch:** \`${branchName}\`
${figmaSection}
## Changes Made
${changesList}

## Screenshots
> Add before/after screenshots here if UI changes were made.

| Before | After |
|--------|-------|
| _n/a_  | _n/a_ |

## Test Cases
${testList}

## Notes for Reviewer
> Add any context that helps the reviewer (edge cases, decisions, known issues).

---
_This PR was generated by the Claude MCP Automation pipeline._
`;
};

/**
 * Builds a complete PR payload (title + body) ready to pass to the GitHub API
 * or the `gh` CLI.
 *
 * @param {import("../tools/ado/get-work-item.js").WorkItem} task
 * @param {object}   [options]
 * @param {string}   [options.branchName]
 * @param {string}   [options.figmaUrl]
 * @param {string}   [options.summary]
 * @param {string[]} [options.changes]
 * @param {string[]} [options.testCases]
 * @returns {{ title: string, body: string }}
 */
export const buildPR = (task, options = {}) => {
  const title = buildPRTitle(task.id, task.title);
  const body = buildPRDescription({
    taskId: task.id,
    taskTitle: task.title,
    taskType: task.type,
    taskUrl: task.url,
    branchName:
      options.branchName ?? `feature/${task.id}-${slugify(task.title)}`,
    figmaUrl: options.figmaUrl ?? "",
    summary: options.summary ?? "",
    changes: options.changes ?? [],
    testCases: options.testCases ?? [],
  });

  return { title, body };
};

// ─────────────────────────────────────────────────────────────────────────────
// Internal helpers
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Simple slugifier — lowercase, remove special chars, spaces → dashes (max 40).
 *
 * @param {string} text
 * @returns {string}
 */
const slugify = (text = "") => {
  return String(text)
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .slice(0, 40);
}
