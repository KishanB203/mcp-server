/**
 * PR Template Builder
 * Generates structured PR titles and descriptions following the team standard.
 */

/**
 * Generate a PR title
 * Format: [Task-ID] Task Name
 */
export function buildPRTitle(taskId, taskTitle) {
  return `[${taskId}] ${taskTitle}`;
}

/**
 * Generate a full PR description from task details
 */
export function buildPRDescription({
  taskId,
  taskTitle,
  taskType = "Task",
  taskUrl = "",
  summary = "",
  changes = [],
  figmaUrl = "",
  testCases = [],
  branchName = "",
}) {
  const changesList = changes.length
    ? changes.map((c) => `- ${c}`).join("\n")
    : "- See diff for full list of changes";

  const testList = testCases.length
    ? testCases.map((t) => `- [ ] ${t}`).join("\n")
    : "- [ ] Manual testing completed\n- [ ] Existing tests pass";

  const figmaSection = figmaUrl
    ? `\n## Design\n- Figma: ${figmaUrl}\n`
    : "";

  return `## Summary
${summary || `Implements ${taskType} #${taskId}: ${taskTitle}`}

## Azure DevOps
- **${taskType}:** [#${taskId} — ${taskTitle}](${taskUrl})
- **Branch:** \`${branchName}\`
${figmaSection}
## Changes Made
${changesList}

## Screenshots
> Add before/after screenshots here if UI changes were made.

| Before | After |
|--------|-------|
| _n/a_  | _n/a_ |

## Test Cases
${testList}

## Notes for Reviewer
> Add any context that helps the reviewer (edge cases, decisions, known issues).

---
_This PR was generated by the Claude MCP Automation pipeline._
`;
}

/**
 * Full PR object ready for GitHub CLI or API
 */
export function buildPR(task, options = {}) {
  const title = buildPRTitle(task.id, task.title);
  const body = buildPRDescription({
    taskId: task.id,
    taskTitle: task.title,
    taskType: task.type,
    taskUrl: task.url,
    branchName: options.branchName || `feature/${task.id}-${slugify(task.title)}`,
    figmaUrl: options.figmaUrl || "",
    summary: options.summary || "",
    changes: options.changes || [],
    testCases: options.testCases || [],
  });

  return { title, body };
}

function slugify(text = "") {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .slice(0, 40);
}
